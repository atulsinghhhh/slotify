export default function LoginPage() {
  const router = useRouter();
  const [loading, setLoading] = useState(false);
  const [email, setEmail] = useState("");
  const [password, setPassword] = useState("");

  const handleCredentialsLogin = async (e: React.FormEvent) => {
    e.preventDefault();
    setLoading(true);

    try {
      const result = await signIn("credentials", {
        email,
        password,
        redirect: false,
      });

      if (result?.error) {
        toast.error("Invalid credentials");
        setLoading(false);
        return;
      }

      // Fetch session to determine role and redirect
      const session = await getSession();
      if (session?.user) {
        // Assume 'role' is part of the session user object
        // We might need to cast to any if TS complains about custom properties on User
        const user = session.user as any; 
        
        toast.success("Logged in successfully");
        
        if (user.role === "provider") { // or "provider" - checking both cases or normalizing
           router.push("/provider/dashboard");
        } else {
           router.push("/");
        }
      } else {
        // Fallback if session fetch fails but login worked (rare)
        router.push("/");
      }

    } catch (error) {
      toast.error("Login unexpected error");
      console.error(error);
      setLoading(false);
    }
  };

  const handleGoogleLogin = () => {
    signIn("google", { callbackUrl: "/" }); // Google usually defaults to customer or existing user. 
    // Ideally duplicate the role check in a client-side effect if needed, but callbackUrl is simpler.
  };

  return (
    <div className="flex justify-center items-center min-h-[calc(100vh-4rem)] bg-muted/20">
      <Card className="w-full max-w-md mx-4">
        <CardHeader>
          <CardTitle>Welcome back</CardTitle>
          <CardDescription>Login to your Slotify account</CardDescription>
        </CardHeader>
        <CardContent className="space-y-4">
          <Button variant="outline" className="w-full" onClick={handleGoogleLogin}>
            <GoogleIcon className="mr-2 h-4 w-4" />
            Sign in with Google
          </Button>

          <div className="relative">
            <div className="absolute inset-0 flex items-center">
              <span className="w-full border-t" />
            </div>
            <div className="relative flex justify-center text-xs uppercase">
              <span className="bg-background px-2 text-muted-foreground">Or continue with</span>
            </div>
          </div>

          <form onSubmit={handleCredentialsLogin} className="space-y-4">
            <div className="space-y-2">
              <Label htmlFor="email">Email</Label>
              <Input
                id="email"
                type="email"
                placeholder="m@example.com"
                required
                value={email}
                onChange={(e) => setEmail(e.target.value)}
              />
            </div>
            <div className="space-y-2">
              <Label htmlFor="password">Password</Label>
              <Input
                id="password"
                type="password"
                required
                value={password}
                onChange={(e) => setPassword(e.target.value)}
              />
            </div>
            <Button type="submit" className="w-full" disabled={loading}>
              {loading ? "Logging in..." : "Login"}
            </Button>
          </form>
        </CardContent>
        <CardFooter className="flex flex-col space-y-4">
          <div className="text-sm text-center text-muted-foreground">
            Don't have an account?{" "}
            <Link href="/signup" className="text-primary hover:underline">
              Sign up
            </Link>
          </div>
        </CardFooter>
      </Card>
    </div>
  );
}
